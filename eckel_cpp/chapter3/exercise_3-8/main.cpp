#include<iostream>
#include<math.h>

using namespace std;

//Exercise 3-8: Write a program that uses all trigonometric functions.

int main(void){
	double angle=45;

	cout<<"sin ("<<angle<<") = "<<sin(angle)<<endl;
	cout<<"cos ("<<angle<<") = "<<cos(angle)<<endl;
	cout<<"tan ("<<angle<<") = "<<tan(angle)<<endl;
	cout<<"acos ("<<angle<<") = "<<acos(angle)<<endl;
	

	return 0;
}
